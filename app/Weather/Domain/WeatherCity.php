<?php

namespace App\Weather\Domain;

final class WeatherCity
{

    private ?string $city;


    /**
     * @return string|null
     */
    public function getCity(): ?string
    {
        return $this->city;
    }

    /**
     * @param string|null $city
     */
    public function setCity(?string $city): void
    {
        $this->city = $city;
    }
}
